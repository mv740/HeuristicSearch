public class Blank {

    private int i;
    private int j;
    private final String name = "B";

    public Blank(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public String getName() {
        return name;
    }
}
